// package ch2.src;

public class PrintExample {
    public static void main(String[] args) {
        System.out.println("---System.out.printlnを使った場合---");
        System.out.println("こんにちは");
        System.out.println("今日もよい天気です");

        System.out.println("---System.out.printを使った場合---");
        System.out.print("こんにちは");
        System.out.print("今日もよい天気です");
    }
}